const LoadingScreen: React.FC = () => {
  const [message, setMessage] = useState(() => loadingMessages[Math.floor(Math.random() * loadingMessages.length)]);

  useEffect(() => {
    const intervalId = setInterval(() => {
      setMessage(prevMessage => {
        let nextMessage;
        do {
          const randomIndex = Math.floor(Math.random() * loadingMessages.length);
          nextMessage = loadingMessages[randomIndex];
        } while (nextMessage === prevMessage && loadingMessages.length > 1);
        return nextMessage;
      });
    }, 2000);

    return () => clearInterval(intervalId);
  }, []);

  return (
    <div className="flex flex-col items-center justify-center bg-white/70 backdrop-blur-sm p-8 rounded-2xl shadow-lg border-4 border-black">
      <div className="w-16 h-16 border-8 border-dashed rounded-full animate-spin border-[#4ECDC4]"></div>
      <h2 className="text-3xl mt-6 text-[#FF6B6B]">{message}</h2>
    </div>
  );
};

